'''
Type Casting
    Converting the data type of a value to another data type.
'''
a = 1 # Integer - int
b = "2" # String - str
c = 3.0 # Floating point number - float
print("\n", a, "\n", b, "\n", c) # \n new line
print() # Blank line, also print("")

#vCasting to Integers - Allow math operations
a = int(a)
b = int(b)
c = int(c)
get_sum = a+b+c
print(f"Sum is {get_sum}") # f string is used
print() # Blank line, also print("")

# Casting to strings
a = str(a)
b = str (b)
c = str(c)
print("One " +a+ ", Two " +b+ ", & Three " +c)
print() # Blank line, also print("")

# Casting to float point number
a = float(a)
b = float(b)
c = float(c)
get_sum = a+b+c
print(f"Total is {get_sum}") # f string is used
